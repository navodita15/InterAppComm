package com.example.datasharing;

public class ProcessData {
    public static int getSum(int a, int b) {
        return a + b;
    }

    public static float getSum(float a, float b) {
        return a + b;
    }

    public static double getSum(double a, double b) {
        return a + b;
    }

    public static long getSum(long a, long b) {
        return a + b;
    }

    public static int getDifference(int a, int b) {
        return a - b;
    }

    public static float getDifference(float a, float b) {
        return a - b;
    }

    public static double getDifference(double a, double b) {
        return a - b;
    }

    public static long getDifference(long a, long b) {
        return a - b;
    }

}
